export default function PikselTavsan({ durum = "normal", renk = "Beyaz" }) {
  const kare = KARELER[durum] || KARELER.normal;
  const renkAilesi = TAVSAN_RENKLER[renk] || TAVSAN_RENKLER.Beyaz;
  const renkPaleti = { ...SABIT_RENKLER, ...renkAilesi };

  const satSay = kare.length;
  const sutSay = kare[0].length;

  return (
    <View style={[styles.izgara, { width: sutSay * KARE, height: satSay * KARE }]}>
      {kare.map((satir, si) => (
        <View key={`row-${si}`} style={styles.satir}>
          {satir.map((r, ki) => (
            <View
              key={`${si}-${ki}`}
              style={[styles.piksel, { backgroundColor: renkPaleti[r] }]}
            />
          ))}
        </View>
      ))}
    </View>
  );
}

const styles = StyleSheet.create({
  izgara: {
    backgroundColor: "transparent",
  },
  satir: {
    flexDirection: "row",
  },
  piksel: {
    width: KARE,
    height: KARE,
  },
});
