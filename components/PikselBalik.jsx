export default function PikselBalik({ durum = "normal", renk = "Turuncu" }) {
    const kare = KARELER[durum] || KARELER.normal;
    // Renk ailesi prop üzerinden alınıyor
    const seciliRenkAilesi = BALIK_RENKLER[renk] || BALIK_RENKLER.Turuncu;

    const renkPaleti = { ...SABIT_RENKLER, ...seciliRenkAilesi };

    const satSay = kare.length;
    const sutSay = kare[0].length;

    return (
        <View style={[styles.izgara, { width: sutSay * KARE, height: satSay * KARE }]}>
            {kare.map((satir, si) => (
                <View key={`row-${si}`} style={styles.satir}>
                    {satir.map((r, ki) => (
                        <View
                            key={`${si}-${ki}`}
                            style={[styles.piksel, { backgroundColor: renkPaleti[r] }]}
                        />
                    ))}
                </View>
            ))}
        </View>
    );
}

const styles = StyleSheet.create({
    izgara: {
        backgroundColor: "transparent",
    },
    satir: {
        flexDirection: "row",
    },
    piksel: {
        width: KARE,
        height: KARE,
    },
});
