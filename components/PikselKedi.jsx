export default function PikselKedi({ durum = "normal", renk = "Beyaz" }) {
  const kare = KARELER[durum] || KARELER.normal;
  const renkSecimi = KEDI_RENKLER[renk] || KEDI_RENKLER.Kahverengi;

  // Ana renk paletini 1'den sonrasıyla birleştir, sabitleri üzerine yaz.
  const renkPaleti = {
    ...renkSecimi,
    ...SABIT_RENKLER
  };

  const satSay = kare.length;
  const sutSay = kare[0].length;

  return (
    <View style={[styles.izgara, { width: sutSay * KARE, height: satSay * KARE }]}>
      {kare.map((satir, si) => (
        <View key={`row-${si}`} style={styles.satir}>
          {satir.map((r, ki) => (
            <View
              key={`${si}-${ki}`}
              style={[styles.piksel, { backgroundColor: renkPaleti[r] }]}
            />
          ))}
        </View>
      ))}
    </View>
  );
}

const styles = StyleSheet.create({
  izgara: {
    backgroundColor: "transparent",
  },
  satir: {
    flexDirection: "row",
  },
  piksel: {
    width: KARE,
    height: KARE,
  },
});
