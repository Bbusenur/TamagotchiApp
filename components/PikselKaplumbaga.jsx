// Renk kodları:
const SABIT_RENKLER = {
    0: "transparent",
    1: "#1F2937", // Dış çizgi siyahımsı
    5: "#000000", // Göz
    6: "#A78BFA", // Uyku Zzz
    7: "#EF4444", // Yemek / Karides kırmızı
    8: "#F472B6", // Kalp veya su baloncuğu
};


export default function PikselKaplumbaga({ durum = "normal", renk = "Kahverengi" }) {
    const kare = KARELER[durum] || KARELER.normal;
    const renkSecimi = KAPLUMBAGA_RENKLER[renk] || KAPLUMBAGA_RENKLER.Kahverengi;
    const renkPaleti = { ...renkSecimi, ...SABIT_RENKLER };

    const satSay = kare.length;
    const sutSay = kare[0].length;

    return (
        <View style={[styles.izgara, { width: sutSay * KARE, height: satSay * KARE }]}>
            {kare.map((satir, si) => (
                <View key={`row-${si}`} style={styles.satir}>
                    {satir.map((r, ki) => (
                        <View
                            key={`${si}-${ki}`}
                            style={[styles.piksel, { backgroundColor: renkPaleti[r] }]}
                        />
                    ))}
                </View>
            ))}
        </View>
    );
}

const styles = StyleSheet.create({
    izgara: {
        backgroundColor: "transparent",
    },
    satir: {
        flexDirection: "row",
    },
    piksel: {
        width: KARE,
        height: KARE,
    },
});
