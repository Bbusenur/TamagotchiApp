// Sabit renk kodları:
const SABIT_RENKLER = {
    0: "transparent",
    5: "#000000", // Göz
    7: "#EF4444", // Ayak
    8: "#A78BFA", // Uyku Zzz (Mor)
    9: "#10B981", // Yemek/Tohum (Yeşil/Kahve vs)
    10: "#F43F5E", // Kalp (Kırmızı/Pembe)

};

export default function PikselKus({ durum = "normal", renk = "Sari" }) {
    const kare = KARELER[durum] || KARELER.normal;
    const renkSecimi = KUS_RENKLER[renk] || KUS_RENKLER.Sari;

    // Ana renk paleti
    const renkPaleti = {
        ...renkSecimi,
        ...SABIT_RENKLER
    };

    // Üzgün durumu için gözyaşı mavisini 10 indeksine override yap:
    if (durum === "uzgun") {
        renkPaleti[10] = "#93C5FD";
    }

    const satSay = kare.length;
    const sutSay = kare[0].length;

    return (
        <View style={[styles.izgara, { width: sutSay * KARE, height: satSay * KARE }]}>
            {kare.map((satir, si) => (
                <View key={`row-${si}`} style={styles.satir}>
                    {satir.map((r, ki) => (
                        <View
                            key={`${si}-${ki}`}
                            style={[styles.piksel, { backgroundColor: renkPaleti[r] }]}
                        />
                    ))}
                </View>
            ))}
        </View>
    );
}

const styles = StyleSheet.create({
    izgara: {
        backgroundColor: "transparent",
    },
    satir: {
        flexDirection: "row",
    },
    piksel: {
        width: KARE,
        height: KARE,
    },
});
